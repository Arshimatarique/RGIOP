 <p style="text-align: justify; color: #000000; line-height: 200%;">1. Students are informed to maintain the highest standard of behaviour and discipline both inside and outside the college.<br>
2. Students are required to improve and maintain consistency in their performance in University or college examination in order to get promotion to the subsequent semester and year.<br>
3. They shall strictly observe the disciplinary rules set by the college. Violations of the principles will address this matter in a manner that will be the final concern for the class faculty.<br>
4. Every student should possess an identity card issued by the college and have to wear it regularly and should be produced when required.
5. The dress code should be followed strictly.<br>
6. No student shall enter or leave the class or lab without the lecturer's permission.<br>
7. The student's attendance, and progress are closely monitored by the class in charge and mentor.<br>
8. Irregular attendance, indifference towards class work and examinations, discourtesy towards teachers, insubordination, obscenity in words and deeds, deliberate harm to college property, and anti-social activities are all grounds for disciplinary actions, which may include explanations from the college.<br>
9. Attendance at college functions, such as association meetings, college assemblies, and seminars, is required. Among other requirements, all students must participate in the personality development program, industrial visit, and Viva Voce.<br>
10. Latecomers can enter the class only if permitted by the faculty.<br>
11. Without the permission of the principal, students shall not organize any activities or associate with any outside group concerned with the college.<br>
12. The principal shall have the power to expel any students from the college if the students are guilty of serious misconduct or student's presence in the college is injurious to the order and discipline.<br>
13. Loitering and wasting time on the college campus and canteen cause indiscipline in the College campus, and that must be avoided.<br>
14. There is nothing that can substitute for self-discipline, a serious devotion to duty, and a spirit of respect and love for all that is good, noble, and beautiful in life.<br>
15. The college expects students to keep their vision high and solicit the cooperation of their parents to minimize the necessity of enforcing rules and regulations.<br>
16. Students are expected to treat the College as their own and to cooperate in its efficient and smooth running.
